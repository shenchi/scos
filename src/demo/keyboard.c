void init_keyboard(void){
	unsigned long i;
	for(i=0;i<300;i++){
		kbuf[i]=0;
	}
	kbuf[0]=0xFF;
	__asm__("mov $0x302,%%eax;int $0x40"::"b"((unsigned long)(&kbuf[0])));
}

unsigned char get_scancode(void){
	unsigned short khead,ktail;
	khead=kbuf[0];
	ktail=kbuf[1];
	khead=(khead+1)%256;
	if(khead==ktail)return 0;
	kbuf[0]=khead&0xFF;
	return kbuf[khead+2];
}

unsigned long translate_scancode(unsigned long scancode){
	if(scancode&0x80==0){
		return skbdus[scancode&0x7F];
	}else{
		return kbdus[scancode];
	}
}

unsigned char get_char(void){
	unsigned char tmpchar;
	tmpchar=get_scancode();
	return kbdus[tmpchar&0xFF];
}

